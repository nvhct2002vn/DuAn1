    public void fillThongTin() {
        NhanVien nv = Auth.user;
        lblMaNV.setText(nv.getMaNV());
        lblTen.setText(nv.getTenNV());
        lblGioiTinh.setText(nv.isGioiTinh() ? "Nam" : "Nữ");
        lblNgaySinh.setText(XDate.toString(nv.getNgaySinh()));
        lblDienThoai.setText(nv.getDienThoai());
        lblDiaChi.setText(nv.getDiaChi());
        lblVaiTro.setText(nv.isVaiTro() ? "Quản lý" : "Nhân viên");
    }
    
    public void doiMatKhau(){
        String matKhau = new String(txtMatKhauHienTai.getPassword());
        String matKhauMoi = new String(txtMatKhauMoi.getPassword());
        String xacNhan = new String(txtXacNhan.getPassword());
        if (matKhau.trim().equals("")) {
            Msgbox.alert(this, "Mật khẩu hiện tại rỗng");
        } else if (matKhauMoi.trim().equals("")) {
            Msgbox.alert(this, "Mật khẩu mới rỗng");
        } else if (xacNhan.trim().equals("")) {
            Msgbox.alert(this, "Xác nhận mật khẩu mới rỗng");
        } else if (!matKhau.equals(Auth.user.getMatKhau())) {
            Msgbox.alert(this, "Sai mật khẩu");
        } else if (!matKhauMoi.equals(xacNhan)) {
            Msgbox.alert(this, "Xác nhận mật khẩu không đúng");
        } else {
            Auth.user.setMatKhau(matKhauMoi);
            nvService.updateData(Auth.user);
            lamMoi();
            Msgbox.alert(this, "Đổi mật khẩu thành công");
        }
    }
    
    public void lamMoi(){
        txtMatKhauHienTai.setText("");
        txtMatKhauMoi.setText("");
        txtXacNhan.setText("");
    }


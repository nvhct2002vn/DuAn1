    public void timKiem() {
        DefaultTableModel model = (DefaultTableModel) tblHoaDon3.getModel();
        model.setRowCount(0);
        String key = txtTimKiem3.getText();
        List<HoaDon> list = hdService.selectByTimKiem(key);
        for (HoaDon x : list) {
            model.addRow(new Object[]{
                x.getMaHD(), x.getTongTien() + " VND", x.getTienThuaTraKhach() + " VND", x.getHinhThuc_TT(), x.getHinhThucGiaoHang(),
                XDate.toString(x.getNgayLap()), x.getTrangThai_TT(), x.getMaNV(), x.getTenNV(), x.getMaKH(), x.getTenKH(), x.getGhiChu()
            });
        }
    }

    public void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblHoaDon3.getModel();
        model.setRowCount(0);
        List<HoaDon> list = hdService.selectAll();
        for (HoaDon x : list) {
            model.addRow(new Object[]{
                x.getMaHD(), x.getTongTien() + " VND", x.getTienThuaTraKhach() + " VND", x.getHinhThuc_TT(), x.getHinhThucGiaoHang(),
                XDate.toString(x.getNgayLap()), x.getTrangThai_TT(), x.getMaNV(), x.getTenNV(), x.getMaKH(), x.getTenKH(), x.getGhiChu()
            });
        }
    }

    public void clickHoaDon() {
        row = tblHoaDon3.getSelectedRow();
        String maHD = tblHoaDon3.getValueAt(row, 0) + "";
        DefaultTableModel model = (DefaultTableModel) tblChiTietHoaDoon.getModel();
        model.setRowCount(0);
        List<HoaDonChiTiet> list = hdctService.selectById(maHD);
        for (HoaDonChiTiet x : list) {
            model.addRow(new Object[]{
                x.getMaHDCT(), x.getMaSPCT(), x.getMSeri(), x.getTenSP() + " " + x.getTenSPCT(), x.getSoLuong(), x.getDonGia() + " VND", x.getDonGia() * x.getSoLuong() + " VND"
            });
        }
    }


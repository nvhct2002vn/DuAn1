    public void Them() {
        try {
            NhanVien nv = getForm();
            daoNV.insertData(nv);
            FillTable();
            FillTableNghiViec();
            Msgbox.alert(this, "Thêm thành công");
        } catch (Exception e) {
            e.printStackTrace();  //in ra lỗi
        }
    }


    void guiMail() {
        try {
            for (int i = 0; i < listNV.size(); i++) {
                if (listNV.get(i).getMaNV().equals(txtMaNV.getText())) {
                    email = listNV.get(i).getEmail();
                    System.out.println(email);
                    break;
                }
            }
            Random random = new Random();
            maGuiVeMail = (int) Math.floor(((Math.random() * 899999) + 100000));
            JavaMail.sendMail("KHÔI PHỤC TÀI KHOẢN.", email, "Mã khôi phục mật khẩu của bạn là: " + maGuiVeMail);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    boolean checkMa() {
        boolean checkMaNe = true;
        if (txtMaNV.getText().length() > 5) {
            Msgbox.alert(this, "Mã chỉ có 6 ký tự");
        } else {
            if (maGuiVeMail == Integer.parseInt(txtCode.getText())) {
                Msgbox.alert(this, "Mã đúng");
            } else {
                Msgbox.alert(this, "Mã sai");
            }
        }
        return checkMaNe;
    }


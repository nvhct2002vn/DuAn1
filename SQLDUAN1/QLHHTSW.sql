CREATE DATABASE QLBHDIENMAY_TSOFTWEAR
GO

USE QLBHDIENMAY_TSOFTWEAR
GO

CREATE TABLE NHOMHANHOA
(
MaNHH VARCHAR(10),
TenNHH NVARCHAR(50),
GhiChu NVARCHAR(200),
PRIMARY KEY (MaNHH)
)
GO

CREATE TABLE NHANVIEN
(
MaNV VARCHAR(10),
TenNV NVARCHAR(50),
DiaChi NVARCHAR(50),
DienThoai VARCHAR(12),
GioiTinh BIT,
NgaySinh DATE,
PRIMARY KEY (MaNV) 
)
GO

CREATE TABLE SANPHAM
(
MaSP VARCHAR(10),
MaNHH VARCHAR(10),
TeNSP NVARCHAR(50),
NhomPhoBien BIT,
NhaSX NVARCHAR(50),
NuocSX NVARCHAR(50),
SoLuong INT,
GiaNhap MONEY,
GiaBan MONEY,
MoTa NVARCHAR(100),
PRIMARY KEY (MaSP),
FOREIGN KEY (MaNHH) REFERENCES NHOMHANHOA (MANHH),
)
GO

CREATE TABLE TAIKHOAN
(
MaNV VARCHAR(10),
MatKhau VARCHAR(100),
VaiTro BIT,
PRIMARY KEY(MaNV),
FOREIGN KEY (MaNV) REFERENCES NHANVIEN (MaNV)
)
GO

CREATE TABLE KHACHHANG
(
MaKH VARCHAR(10),
TenKH NVARCHAR(50),
DIENTHOAI VARCHAR(12),
EMAIL VARCHAR(50),
DIACHI NVARCHAR(50),
PRIMARY KEY (MaKH)
)
GO

CREATE TABLE HOADON
(
MaHD VARCHAR(10),
MaNV VARCHAR(10),
MaKH VARCHAR(10),
NgayLap DATE,
TrangThaiTT NVARCHAR(30),
HinhThucTT NVARCHAR(30),
TongTien MONEY,
GhiChu NVARCHAR(100),
PRIMARY KEY (MaHD),
FOREIGN KEY (MaNV) REFERENCES NHANVIEN (MaNV),
FOREIGN KEY (MaKH) REFERENCES KHACHHANG(MaKH),
)
GO

CREATE TABLE HOADONDOITRA(
MaHDDT VARCHAR(10),
MaHD VARCHAR(10),
MaNV VARCHAR(10),
DonGia MONEY,
NgayLap DATE,
MoTa NVARCHAR(100),
PRIMARY KEY (MaHDDT),
FOREIGN KEY (MaHD) REFERENCES HOADON (MaHD),
FOREIGN KEY (MaNV) REFERENCES NHANVIEN (MaNV)
) 
GO

CREATE TABLE HOADONCHITIET
(
MaHD VARCHAR(10),
MaSP VARCHAR(10),
SoLuong INT,
DonGia MONEY,
GiamGia MONEY,
ThanhTien MONEY,
PRIMARY KEY (MaHD , MaSP ) ,
FOREIGN KEY (MaHD) REFERENCES HOADON (MaHD),
FOREIGN KEY (MaSP) REFERENCES SANPHAM (MaSP)
)
GO

CREATE TABLE VOUCHER 
(
MaVOUCHER VARCHAR(10),
MaNHH VARCHAR(10),
NgayTao DATE,
NgayHetHan DATE,
SoLuong INT,
MenhGia MONEY,
TrangThai BIT,
GhiChu NVARCHAR(100),
PRIMARY KEY (MaVOUCHER),
FOREIGN KEY (MaNHH) REFERENCES NHOMHANHOA (MaNHH)
)
GO

CREATE TABLE KHUYENMAI
(
MaKM VARCHAR(10),
MaNHH VARCHAR(10),
TenCT NVARCHAR(50),
HinhThuc NVARCHAR(100),
BatDau DATE,
KetThuc DATE,
GiamGia FLOAT,
MoTa NVARCHAR(100),
PRIMARY KEY (MaKM),
FOREIGN KEY(MaNHH) REFERENCES NHOMHANHOA (MaNHH)
)
GO


